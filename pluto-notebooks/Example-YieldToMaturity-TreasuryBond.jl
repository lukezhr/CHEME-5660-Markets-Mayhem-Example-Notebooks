# ╔═╡ bdd414e3-84dc-4bd4-adf4-48fe95c26c7e
md"""
a) __Compute__: YTM for a bond with a par value of Vₚ = \$1000 USD, a sale price of V̂ᵦ = \$1476.76 USD, a term of T = 30 years, and an annual coupon rate of c̄ = 8%.
"""

# ╔═╡ fe109196-afa0-4ab8-9440-6aaf609d1915
begin

	# setup the calculation - 
	xinitial = [0.001]
	
	# setup bounds -
	L = 0.00001
	U = 0.99999

	# Data from BKM Example 14.4
	Vₚ = 1000.0 	# par of the bond
	V̂ᵦ = 1476.76 	# price of the bond
	T = 30 			# term of the bond
	c̄ = 0.08 		# annual coupon rate
	
	# setup the objective function -
	OF(p) = loss(p, V̂ᵦ, Vₚ, T, c̄)
    
    # call the optimizer -
    opt_result = optimize(OF, L, U, xinitial, Fminbox(BFGS()))

	# show -
	nothing
end

# ╔═╡ b0ea113a-a2c1-49f7-8e5f-69d12e60ce55
with_terminal() do
	
	bgfs_soln = Optim.minimizer(opt_result)[1]
	println("Yield to Maturity (YTM) annual rate r̄ = $(bgfs_soln*100)%")
end

# ╔═╡ c976f0aa-4c79-4511-9f64-27c90d6ca4f1
md"""
b) __Check__: can we recover the sale price if we plug in the YTM value for r̄ in the `price` function?
"""

# ╔═╡ 24a27483-f1a4-4b3e-a06f-2fbc0937c694
begin

	# get the value of the optimal solution from the Optim result object -
	r̄_check =  Optim.minimizer(opt_result)[1]

	# compute the price?
	V̂ᵦ_check = price(Vₚ, T, c̄, r̄_check)

	# display -
	with_terminal() do
		println("Check: V̂ᵦ_check = $(V̂ᵦ_check) USD")
	end
end

