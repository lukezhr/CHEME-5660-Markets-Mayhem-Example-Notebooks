# ╔═╡ eac2d9cb-5f06-401c-a217-4ddda7f5cee3
# ------------------------------------------------------------------------------------------- #
# Fill me in with some awesome stuff
# ------------------------------------------------------------------------------------------- #
function price(Vₚ::Float64, T::Int64, c̄::Float64, r̄::Float64)

	# initialize -
	i = (r̄/2)
	C = (c̄/2)*Vₚ
	T′ = 2*T
	
	# compute the final payout -
	final_payout = Vₚ/((1+i)^(T′))
	coupon_payments = Array{Float64,1}()
	
	# main loop -
	for t ∈ 1:T′

		# compute the present value of future coupon payments in year t -
		value = C/((1+i)^(t))

		# capture -
		push!(coupon_payments, value)
	end

	# compute the Vᵦ -
	return (final_payout+sum(coupon_payments))
end

# ╔═╡ 829bc347-222c-4d9d-be96-ec52f0c61fca
# ------------------------------------------------------------------------------------------- #
# Fill me in with some awesome stuff
# ------------------------------------------------------------------------------------------- #
function loss(κ, V̂ᵦ::Float64, Vₚ::Float64, T::Int64, c̄::Float64)::Float64

	# unpack the unknown parameter vector -
	r̄ = κ[1] # guess for the market interest rate 

	# compute the test Vᵦ -
	Vᵦ = price(Vₚ, T, c̄, r̄)

	# return sq-diff -
	return ((V̂ᵦ - Vᵦ)^2)
end

